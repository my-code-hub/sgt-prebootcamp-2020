//13) Write a Java program to display the following text. Go to the editor
//   Sample Pattern :
//       J    a   v     v  a
//       J   a a   v   v  a a
//    J  J  aaaaa   V V  aaaaa
//     JJ  a     a   V  a     a
public class Exercise13 {

    public static void main(String[] args) {
        //ASCII art
        String java =
         "    J    a   v     v  a   \n" +
         "    J   a a   v   v  a a  \n" +
         " J  J  aaaaa   V V  aaaaa \n" +
         "  JJ  a     a   V  a     a";
        System.out.println(java);
    }
}
