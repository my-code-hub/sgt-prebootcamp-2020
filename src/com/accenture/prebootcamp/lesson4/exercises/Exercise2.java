package com.accenture.prebootcamp.lesson4.exercises;

//2) Write a Java program to declare two integer
//   variables, one float variable, and one string variable and
//   assign 10, 12.5, and "Java programming" to them respectively.
//   Then display their values on the screen.
public class Exercise2 {

    public static void main(String[] args) {
        int number1 = 10;
        int number2;
        number2 = 10;
        float myFloat = 12.5f;
        String myString = "Java programming";

        System.out.println(number1);
        System.out.println(number2);

        System.out.println(myFloat + "\n" + myString);
    }
}
