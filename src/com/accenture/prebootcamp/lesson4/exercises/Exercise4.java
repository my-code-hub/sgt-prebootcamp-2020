//4)  a) Declare two float variables (x and y) and assign them values 2.5 and 5.4
// (with separated declaration and initialization)
//    b) Declare two float variables (a and b) and assign them values 3.5 and 6.4
//   (declaration and initialization in the same statement)
public class Exercise4 {

    public static void main(String[] args) {
        //a)  Declare two float variables (x and y) and assign them values 2.5 and 5.4
        //// (with separated declaration and initialization)
        float x, y;
        x = 2.5f;
        y = 5.4f;

        System.out.println(x);
        System.out.println(y);

        //b) Declare two float variables (a and b) and assign them values 3.5 and 6.4
        //  (declaration and initialization in the same statement)
        float a = 3.5f, b = 6.4f;

        System.out.println(a);
        System.out.println(b);
    }
}
